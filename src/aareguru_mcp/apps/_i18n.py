def t(key: str, lang: str = DEFAULT_LANG) -> str:
    """Return a translated string; falls back to German for missing keys."""
    locale = lang if lang in LOCALES else DEFAULT_LANG
    result = STRINGS.get(locale, {}).get(key)
    if result is not None:
        return result
    return STRINGS[DEFAULT_LANG].get(key, key)
